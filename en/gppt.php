<section class="rules team-rules">
  <h2>Organisational announcement for BUDIMEX Grand Prix Polski Teamów 2026</h2>
  <p class="rules__intro">The tournament is played as part of the 65th International Baltic Congress in Sopot.</p>

  <section class="rules__section">
    <h3>I. General information</h3>
    <ol>
      <li>The organiser of the BUDIMEX Grand Prix Polski Teamów 2026 tournament in Sopot is the Pomeranian Regional Sports Bridge Association.</li>
      <li>The tournament is played on 27-29.07.2026 at Hala Stulecia in Sopot, ul. Goyki 7.</li>
      <li>Entries are accepted together with payment through the registration system.</li>
      <li>The tournament is governed by PZBS regulations and the <a target="_blank" href="https://www.pzbs.pl/regulaminy-stale/3540-regulamin-grand-prix-polski-teamow">PZBS Grand Prix Polski Teamów Regulations</a>.</li>
      <li>The schedule, entry fee amounts and registration link are available on the Congress website.</li>
      <li>Teams may consist of 4-6 players.</li>
      <li>In special cases, the Chief Tournament Director may decide to change the format of play to the extent necessary for the efficient running of the event.</li>
    </ol>
  </section>

  <section class="rules__section">
    <h3>II. System of play</h3>
    <ol>
      <li>The tournament is scored in IMPs converted to VPs.</li>
      <li>GPPT will be played in two phases:
        <ul>
          <li>qualifying stage,</li>
          <li>knockout stage.</li>
        </ul>
      </li>
      <li>The qualifying stage will be played as a Swiss movement without repeats.</li>
      <li>The exact number of rounds and boards per round will be announced after entries close, taking into account the number of teams and the requirements of the central GPPT regulations.</li>
      <li>For the first round, teams will be seeded based on the PZBS Ranking according to the sum of ranking points of the 4 highest-ranked players.</li>
      <li>Teams from 1 to n/2 will be home teams, and teams from n/2+1 to n will be away teams. If the number of teams is odd, the middle team or another team indicated for organisational reasons receives a bye in the first round.</li>
      <li>A team with a bye receives 12 VP in that round.</li>
      <li>8 teams qualify for the knockout stage.</li>
    </ol>
  </section>

  <section class="rules__section">
    <h3>III. Ranking after the qualifying stage</h3>
    <p>In the event of an equal number of VPs, the higher place is decided in order by:</p>
    <ol>
      <li>higher place before the last round, and then before the penultimate round,</li>
      <li>Buchholz score, i.e. the sum of opponents' results,</li>
      <li>greater IMP difference,</li>
      <li>draw.</li>
    </ol>
  </section>

  <section class="rules__section">
    <h3>IV. Knockout stage</h3>
    <ol>
      <li>Knockout matches are 32-board matches, 4 x 8 boards.</li>
      <li>Teams receive numbers 1-8 according to their ranking after the qualifying stage.</li>
      <li>Opponent selection takes place immediately after the protest period for the completed stage expires. If a team representative is absent during selection, the team is automatically paired with the team holding the highest unselected number.</li>
      <li>In the quarter-final, teams 1-3 choose opponents in order from teams 4-8.</li>
      <li>In the semi-final, team 1 chooses its opponent from the remaining teams, with the reservation that match winners take over the opponent's number if the opponent had a lower number.</li>
      <li>Quarter-final losers finish the event in places 5-8 according to the ranking after the qualifying stage.</li>
      <li>Semi-final winners advance to the final, and losers play the match for 3rd place.</li>
      <li>The home team is the team with the lower number.</li>
      <li>The home team sits second in segments 2 and 4.</li>
      <li>In the knockout stage, the home team receives a carry-over equal to 10% of the number of boards scheduled for the match, plus an additional 3, 2, 1 IMP for the first three teams after the qualifying stage.</li>
      <li>If a team takes over another team's number, it is entitled only to a carry-over equal to 10% of the number of boards scheduled for the match, without the additional bonus.</li>
      <li>If the number of boards in a play-off match is reduced with the consent of the Chief Tournament Director, the carry-over does not change.</li>
    </ol>
  </section>

  <section class="rules__section">
    <h3>V. Protest period and score corrections</h3>
    <ol>
      <li>The protest period under Law 79C1 of the Laws of Duplicate Bridge expires:
        <ul>
          <li>in the qualifying stage - 15 minutes after the start of the next match, and for the last match 15 minutes after the last score is received,</li>
          <li>in knockout matches - 15 minutes after the last score is received.</li>
        </ul>
      </li>
      <li>A scoring error may be corrected after the protest period under Law 79C2 with the consent of the Chief Tournament Director.</li>
      <li>Such a score change may not affect prizes already awarded or matches already started in the knockout or qualifying stage.</li>
      <li>The Chief Tournament Director decides each time whether such a change may affect prizes not yet paid, the Congress long-term ranking or victory in a knockout match.</li>
    </ol>
  </section>

  <section class="rules__section">
    <h3>VI. Prizes and points</h3>
    <ol>
      <li>The tournament prize fund is at least 40% of entry fees.</li>
      <li>Financial prizes are paid to the team captain.</li>
      <li>GPPT long-term points are awarded according to the central PZBS regulations.</li>
      <li>A player receives GPPT points provided they play the minimum number of boards required by the central regulations.</li>
    </ol>
  </section>
</section>

