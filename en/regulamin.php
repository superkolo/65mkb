  <h2>65th International Baltic Congress - Tournament Rules</h2>

  <section class="rules__section">
    <h3>I. General provisions</h3>
    <ol>
      <li>The organiser of the 65th International Baltic Congress (MKB) is the Pomeranian Regional Sports Bridge Association. The MKB is played at Hala Stulecia in Sopot, ul. Goyki 7, on 24-30.07.2026.</li>
