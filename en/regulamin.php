      <li>The events are open, with the following exceptions:
        <ul>
          <li>foreign players from Russia and Belarus who are not affiliated with PZBS may not participate in the tournaments,</li>
          <li>GPPP and GPPT tournaments are open only to affiliated players with a paid current PZBS membership fee or foreign players affiliated with their home sports bridge federation that is a member of the WBF,</li>
          <li>the Amateur Tournament is open to players not affiliated with PZBS or PZBS players up to 1.5 WK. In special cases, the Chief Tournament Director may admit a player with a higher WK or refuse admission to an unaffiliated person.</li>
        </ul>
      </li>
      <li>The relevant PZBS regulations apply in the tournaments.</li>
      <li>Pursuant to § 39 section 2 of the PZBS Tournament Regulations, the organiser introduces the following differentiated penalties:
        <ul>
          <li>minimum penalty:
            <ul>
              <li>in a matchpoint tournament - 10% of the points available in one board,</li>
              <li>in an IMP pairs tournament and in a team tournament play-off phase - 1 IMP,</li>
              <li>in a match converted to VP - 0.5 VP,</li>
            </ul>
          </li>
          <li>procedural penalty - respectively 25% / 2 IMP / 1 VP,</li>
          <li>maximum penalty - respectively 50% / 5 IMP / 2 VP.</li>
        </ul>
      </li>
      <li>In special cases, the Chief Tournament Director may permit a penalty of a different amount.</li>
    </ol>
  </section>

  <section class="rules__section">
    <h3>II. Long-term scoring</h3>
    <ol>
      <li>The following long-term rankings will be kept as part of the MKB:
        <ul>
          <li>Congress Ranking - based on the 9 best results, provided that to receive a prize a player must play at least 50% of the tournaments available to play,</li>
          <li>Amateur Ranking - based on results from all Amateur Tournaments.</li>
        </ul>
      </li>
      <li>Tournament winners receive long-term points (pdf) as follows:
        <ul>
          <li>GPPP tournament - 450 pdf,</li>
          <li>pairs tournaments, except the XI pairs tournament - 300 pdf,</li>
          <li>XI pairs tournament - 400 pdf,</li>
          <li>GPPT qualifying stage - 400 pdf,</li>
          <li>Amateur Tournaments - 100 pdf.</li>
        </ul>
      </li>
      <li>In addition, players participating in the GPPT play-off phase receive PDF points as separate tournaments:
        <ul>
          <li>in quarter-finals: winners 264, losers 198,</li>
          <li>in the final ranking for places I - 600, II - 520, III - 440, IV - 360.</li>
        </ul>
      </li>
      <li>In the team tournament, a player receives PDF points provided they play at least 25% of the number of boards scheduled for the given stage. To receive PDF points in the final ranking, this condition must be met in the semi-final and final / third-place match.</li>
      <li>In tournaments, 100% of participants receive pdf points, so that the last place receives 1 pdf. The reduction is an integer value resulting from the number of participants. The reduction may differ by 1 pdf if (pdf points for 1st place) / (number of pairs - 1) is not an integer. In such a case, initial places receive the reduction rounded up and the remaining places rounded down (e.g. with 180 pairs and 400 pdf for first place, the first 41 reductions are 3 pdf and the next ones 2 pdf).</li>
      <li>In addition, top finishers receive additional pdf points according to the following key:
        <ul>
          <li>3x10 pairs tournaments: 30, 21, 15, 10, 7, 5, 3, 2, 1,</li>
          <li>4x10 pairs tournament: 35, 25, 17, 13, 10, 7, 5, 3, 2, 1,</li>
          <li>GPPP tournament: 40, 28, 20, 14, 10, 7, 5, 3, 2, 1,</li>
          <li>amateur tournaments: 5, 3, 1.</li>
        </ul>
      </li>
      <li>In the event of the same number of pdf points, the higher result obtained in tournaments decides in the following order: GPPT, GPPP, 4x10 pairs. If the same places are obtained in all of the above tournaments, players share the place ex aequo and prizes are divided. In Amateur Tournaments, in the event of the same number of pdf points, the higher result obtained in tournaments decides in the following order: teams tournament, then pairs tournaments starting from the most numerous; in the case of equal numbers of participants, the tournament with the higher number (played later) takes precedence.</li>
    </ol>
  </section>

  <section class="rules__section">
    <h3>III. Collection of financial prizes</h3>
    <ol>
      <li>Prizes are paid to the bank account indicated by the player. If a player entitled to receive a prize does not claim it by 31 August 2026, the prize is forfeited.</li>
      <li>Prizes in the teams tournament are paid to the team captain.</li>
    </ol>
  </section>

