<h2>Model organizacyjny z poprzedniej edycji</h2>
<ul class="content-list">
  <li>Eliminacje były rozgrywane w formacie 10 meczów 6-rozdaniowych na dochodzenie bez powtórzeń.</li>
  <li>Do fazy pucharowej awansowało 8 drużyn.</li>
  <li>Play-off był rozgrywany meczami 32-rozdaniowymi (4 x 8 rozdań).</li>
  <li>W ćwierćfinale przeciwników wybierały najwyżej sklasyfikowane drużyny po eliminacjach.</li>
  <li>Zwycięzcy półfinałów awansowali do finału, przegrani grali mecz o 3. miejsce.</li>
  <li>Fundusz nagród stanowił co najmniej 50% wpisowego.</li>
</ul>

