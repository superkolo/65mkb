  <h2>65. Międzynarodowy Kongres Bałtycki - Regulamin Zawodów</h2>

  <section class="rules__section">
    <h3>I. Postanowienia ogólne</h3>
    <ol>
      <li>Organizatorem 65. Międzynarodowego Kongresu Bałtyckiego (MKB) jest Pomorski Wojewódzki Związek Brydża Sportowego. MKB rozgrywany jest w Hali Stulecia w Sopocie przy ul. Goyki 7 w dniach 24-30.07.2026.</li>
      <li>Na terenie obiektu obowiązuje całkowity zakaz palenia, złamanie zakazu podlega karze w wysokości 500 zł.</li>
      <li>Zawody mają charakter otwarty - z wyjątkami:
        <ul>
          <li>w turniejach nie mogą brać udziału zawodnicy zagraniczni z Rosji i Białorusi, którzy nie są zrzeszeni w PZBS,</li>
          <li>w turniejach GPPP, MPT mogą uczestniczyć wyłącznie zawodnicy zrzeszeni (opłacona aktualna składka PZBS) lub zawodnicy zagraniczni zrzeszeni w macierzystym związku brydża sportowego, który jest członkiem WBF,</li>
          <li>w Turnieju Amatorów mogą uczestniczyć zawodnicy niezrzeszeni w PZBS lub zawodnicy PZBS do 1.5 WK. W szczególnych przypadkach sędzia główny może dopuścić zawodnika o wyższym WK lub nie dopuścić osoby niezrzeszonej.</li>
        </ul>
      </li>
      <li>W turniejach stosuje się właściwe przepisy PZBS.</li>
      <li>W oparciu o § 39 ust. 2 Regulaminu Zawodów PZBS organizator wprowadza następujące zróżnicowanie kar:
        <ul>
          <li>kara minimalna wynosząca:
            <ul>
              <li>w przypadku turnieju na maksy - 10% punktów możliwych do zdobycia w jednym rozdaniu,</li>
              <li>w przypadku turnieju na punkty meczowe i turnieju teamów w fazie playoff - 1 imp,</li>
              <li>w przypadku meczu przeliczonego na VP - 0.5 VP,</li>
            </ul>
          </li>
          <li>kara porządkowa - odpowiednio 25%/2 IMP/1 VP,</li>
          <li>kara maksymalna - odpowiednio 50%/5 IMP/2 VP.</li>
        </ul>
      </li>
      <li>W szczególnych przypadkach Sędzia Główny może zezwolić na wymierzenie kary w innej wysokości.</li>
    </ol>
  </section>

  <section class="rules__section">
    <h3>II. Punktacja Długofalowa</h3>
    <ol>
      <li>W ramach MKB prowadzone będą klasyfikacje długofalowe:
        <ul>
          <li>Kongresowa - na podstawie 9 najlepszych wyników z tym, że aby otrzymać nagrodę należy rozegrać minimum 50% turniejów możliwych do rozegrania,</li>
          <li>Amatorów - na podstawie wyników wszystkich Turniejów Amatorów.</li>
        </ul>
      </li>
      <li>Zwycięzcy turniejów otrzymują punkty długofalowe (pdf) odpowiednio:
        <ul>
          <li>w turnieju GPPP - 450 pdf,</li>
          <li>w turniejach par, oprócz XI turnieju par - 300 pdf,</li>
          <li>w XI turnieju par - 400 pdf,</li>
          <li>w eliminacjach MPT - 400 pdf,</li>
          <li>w turniejach Amatorów - 100 pdf.</li>
        </ul>
      </li>
      <li>Ponadto zawodnicy uczestniczący w fazie play-off MPT otrzymują PDFy jako osobne turnieje:
        <ul>
          <li>w ćwierćfinałach: wygrani 264, przegrani 198,</li>
          <li>w klasyfikacji końcowej za miejsca I - 600, II - 520, III - 440, IV - 360.</li>
        </ul>
      </li>
      <li>W turnieju teamów zawodnik otrzymuje PDF pod warunkiem rozegrania min. 25% liczby rozdań przewidzianych do rozegrania w danym etapie. Aby otrzymać pdfy w klasyfikacji końcowej należy spełnić ten warunek w półfinale oraz finale / meczu o trzecie miejsce.</li>
      <li>W turniejach pdf zdobywa 100% uczestników, w ten sposób, by ostatnie miejsce otrzymało 1 pdf. Obniżka przyjmuje wartość całkowitą, wynikającą z liczby uczestników. Obniżka może się zmienić o 1 pdf, w przypadku, gdy (liczba pdf dla 1m.) / (liczba par - 1) nie jest liczbą całkowitą. W takim przypadku początkowe miejsca dostają obniżkę z zaokrągleniem w górę, a pozostałe z zaokrągleniem w dół (np. przy 180 parach przy 400 pdf dla pierwszego miejsca pierwsze 41 obniżek ma wartość 3 pdf, a następne 2 pdf).</li>
      <li>Ponadto zdobywcy czołowych miejsc otrzymają dodatkowe pdf wg klucza:
        <ul>
          <li>w turniejach par 3x10: 30, 21, 15, 10, 7, 5, 3, 2, 1,</li>
          <li>w turnieju par 4x10: 35, 25, 17, 13, 10, 7, 5, 3, 2, 1,</li>
          <li>w turnieju GPPP: 40, 28, 20, 14, 10, 7, 5, 3, 2, 1,</li>
          <li>w turniejach amatorów: 5, 3, 1.</li>
        </ul>
      </li>
      <li>W przypadku takiej samej liczby pdf decyduje wyższy wynik uzyskany w turniejach według kolejności: MPT, GPPP, par 4x10. W przypadku uzyskania takich samych lokat we wszystkich wymienionych wyżej turniejach zawodnicy zajmują miejsce ex aequo, a nagrody ulegają podziałowi. W turniejach Amatorów w przypadku takiej samej liczby pdf decyduje wyższy wynik uzyskany w turniejach wg kolejności: turniej teamów, turnieje par poczynając od najliczniej obsadzonych, w przypadku równych liczb uczestników wcześniejszy jest o wyższym numerze (rozgrywany później).</li>
    </ol>
  </section>

  <section class="rules__section">
    <h3>III. Odbiór nagród finansowych</h3>
    <ol>
      <li>Nagrody wypłacane są na konto wskazane przez zawodnika. Jeżeli zawodnik uprawniony do odbioru nagrody nie zgłosi się po nią do 31 sierpnia 2026, to nagroda przepada.</li>
      <li>Nagrody w turnieju teamów wypłacane są kapitanowi drużyny.</li>
    </ol>
  </section>

