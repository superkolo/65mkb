<h1>Regulamin Kongresu</h1>

<p class="note"><strong>Uwaga:</strong> poniższa treść jest roboczym uzupełnieniem strony na bazie opublikowanego regulaminu poprzedniej edycji. Finalny regulamin 2026 będzie aktualizowany po publikacji przez organizatora.</p>

<h2>I. Postanowienia ogólne</h2>
<ul class="content-list">
  <li>Organizatorem Międzynarodowego Kongresu Bałtyckiego jest Pomorski Wojewódzki Związek Brydża Sportowego.</li>
  <li>Miejscem gry jest Hala Stulecia w Sopocie, ul. Jakuba Goyki 7.</li>
  <li>Zawody mają charakter otwarty, z ograniczeniami wynikającymi z regulaminów PZBS.</li>
  <li>W turniejach GPPP i teamów mogą uczestniczyć wyłącznie zawodnicy z aktualną składką PZBS albo zawodnicy zagraniczni zarejestrowani w swojej federacji.</li>
  <li>W turniejach amatorskich mogą uczestniczyć zawodnicy niezrzeszeni w PZBS albo zrzeszeni do WK 1.5; w szczególnych przypadkach decyzję podejmuje sędzia główny.</li>
  <li>W turniejach stosuje się aktualne przepisy PZBS.</li>
</ul>

<h2>II. Punktacja długofalowa</h2>
<ul class="content-list">
  <li>Prowadzone są klasyfikacje długofalowe: kongresowa oraz amatorów.</li>
  <li>Klasyfikacja kongresowa była liczona na podstawie 9 najlepszych wyników; do odbioru nagrody wymagane było rozegranie minimum 50% możliwych turniejów.</li>
  <li>Zwycięzcy turniejów otrzymują punkty długofalowe zależnie od rangi turnieju: GPPP, turniejów par 3x10, turnieju 4x10, teamów i amatorów.</li>
  <li>W przypadku równej liczby punktów decydują wyniki w turniejach wyższej rangi, w szczególności teamów, GPPP i turnieju 4x10.</li>
</ul>

<h2>III. Odbiór nagród</h2>
<ul class="content-list">
  <li>Nagrody finansowe są wypłacane na konto wskazane przez zawodnika.</li>
  <li>W turnieju teamów nagrody wypłacane są kapitanowi drużyny.</li>
</ul>

<h2>IV. Postanowienia końcowe</h2>
<ul class="content-list">
  <li>Sprawy organizacyjne rozstrzyga organizator, a sportowe — sędzia główny.</li>
  <li>Uczestnictwo w zawodach oznacza zgodę na wykorzystanie wizerunku do promocji wydarzenia oraz na przetwarzanie danych osobowych niezbędnych do organizacji zawodów i publikacji wyników.</li>
  <li>Organizator zastrzega sobie możliwość zmiany regulaminu.</li>
</ul>

