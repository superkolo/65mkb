      <li>Ponadto uczestnicy wyrażają zgodę na przetwarzanie danych osobowych na potrzeby organizacji MKB, prowadzenia i prezentowania klasyfikacji sportowych. Dane mogą być udostępniane Polskiemu Związkowi Brydża Sportowego w celu prawidłowej realizacji usług określonych w regulaminie. Zgoda na przetwarzanie danych osobowych może zostać cofnięta po wysłaniu maila z odpowiednią informacją na biuro@bridge.gda.pl.</li>
      <li>Organizator zastrzega sobie możliwość zmiany powyższego regulaminu.</li>
    </ol>
